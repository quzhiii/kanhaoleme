export function searchExperiences(query: string): Experience[] {
  const normalized = query.trim().toLowerCase();
  if (!normalized) return mockExperiences.slice(0, 3);

  const topicMatchers: Array<[string[], string]> = [
    [['减重', '体重', '体检', '营养'], 'weight'],
    [['牙', '智齿', '洗牙', '补牙'], 'dental'],
    [['眼', '干眼', '验光', '配镜'], 'eye'],
    [['皮肤', '祛斑', '美容', '斑'], 'skin'],
    [['肩', '颈', '康复', '运动', '产后'], 'rehab'],
    [['中医', '理疗', '治未病', '艾灸', '社康'], 'tcm'],
  ];

  const matchedTopic = topicMatchers.find(([words]) => words.some((word) => normalized.includes(word)))?.[1];
  const matchedCity = ['杭州', '广州', '北京'].find((city) => normalized.includes(city));

  const results = mockExperiences.filter((item) => {
    const topicOk = matchedTopic ? item.topic === matchedTopic : true;
    const cityOk = matchedCity ? item.city === matchedCity : true;
    const textOk = [item.title, item.summary, item.topicLabel, item.city, ...item.tags].some((text) =>
      text.toLowerCase().includes(normalized),
    );
    return (matchedTopic || matchedCity) ? topicOk && cityOk : textOk;
  });

  return (results.length ? results : mockExperiences).slice(0, 3);
}
